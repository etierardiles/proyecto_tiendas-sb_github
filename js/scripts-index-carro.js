function aumentar_cantidad() {
    let inputCantidad = document.getElementById("cantidad");
    let cantidad = parseInt(inputCantidad.value);
    inputCantidad.value = cantidad + 1;
}
function disminuir_cantidad() {
    let inputCantidad = document.getElementById("cantidad");
    let cantidad = parseInt(inputCantidad.value);
    if (cantidad > 1) {
        inputCantidad.value = cantidad - 1;
    }
}
